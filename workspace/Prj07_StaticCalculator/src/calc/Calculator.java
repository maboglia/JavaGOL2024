package calc;

public class Calculator {

	/**
	 * Metodo addizione, ritorna la somma dei 2 param
	 * @param a - inserire il primo numero intero
	 * @param b - inserire il secondo numero intero
	 * @return Ritorna l'addizione tra i due numeri passati come argomento
	 */
	public static int addizione(int a, int b) {
		return a + b;
	}
	
	/**
	 * Metodo sottrazione, ritorna la sottrazione dei 2 param
	 * @param a - inserire il primo numero intero
	 * @param b - inserire il secondo numero intero
	 * @return Ritorna la sottrazione tra i due numeri passati come argomento
	 */
	public static int sottrazione(int a, int b) {
		return a - b;
	}
	
	/**
	 * Metodo moltiplicazione, ritorna il prodotto dei 2 param
	 * @param a - inserire il primo numero intero
	 * @param b - inserire il secondo numero intero
	 * @return Ritorna la moltiplicazione tra i due numeri passati come argomento
	 */
	public static int moltiplicazione(int a, int b) {
		return a * b;
	}
	
	/**
	 * Metodo divisione, ritorna il quoziente dei 2 param
	 * @param a - inserire il primo numero intero
	 * @param b - inserire il secondo numero intero
	 * @return Ritorna la divisione tra i due numeri passati come argomento
	 */
	public static double divisione(int a, int b) {
		return  (double) a / b;
	}
	
	
}
